#include <stdio.h>

int main() {
    int a = 10, b = 5, x = 1, y = 0;

    // Arithmetic Operations
    printf("Sum: %d, Product: %d\n", a + b, a * b);

    // Relational Operators
    printf("a > b: %d, a == b: %d, a != b: %d\n", a > b, a == b, a != b);

    // Logical Operators
    printf("x && y: %d, x || y: %d, !x: %d\n", x && y, x || y, !x);

    return 0;
}

